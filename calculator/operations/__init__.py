""" Operations Class """


class Addition:
    @staticmethod
    def add(value1, value2):
        return value1 + value2


class Subtraction:
    @staticmethod
    def subtract(value1, value2):
        return value1 - value2


class Multiplication:
    @staticmethod
    def multiply(value1, value2):
        return value1 * value2


class Division:
    @staticmethod
    def divide(value1, value2):
        return value1 / value2